package basics;

public class BasicsQ7 {

	public static void main(String[] args) {
		int a = 5;
		int b = 10;
		boolean isEqual = (a==b);
		boolean isNotEqual = (a!=b);
		boolean isGreater = (a>b);
		boolean isLess = (a<b);
		
		System.out.println("Are numbers equal ? " + isEqual);
		System.out.println("Are numbers not equal ? " + isNotEqual);
		System.out.println("Is the first number greater ? " + isGreater);
		System.out.println("Is the first number less ? " + isLess);
	}

}
