package basics;

public class Variable {

	public static void main(String[] args) {
		int x;     // declaration
		x=5;       // initialization
		System.out.println(x);
		System.out.println("x");
		
		int y=10;     // declaration and initialization
		System.out.println(y);
		System.out.println(x+y);
	}

}
