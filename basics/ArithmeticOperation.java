package basics;

public class ArithmeticOperation {

	public static void main(String[] args) {
		// int data type
		int x = 25;
		int y = 10;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		
		// float and double data type
		double a = 25;
		double b = 10;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
	}

}
