package basics;

public class BasicsQ3 {

	public static void main(String[] args) {
		int x = 25;
		int y = 10;
		System.out.println("Integer result : " + (x-y));
		
		float i = 24.6f;
		float j = 2.0f;
		System.out.println("Float result : " + (i/j));
		
		double a = 21.2;
		double b = 3.3;
		System.out.println("Double result : " + (a+b));
		
		double t = 25.0;
		double s = 5.0;
		System.out.println("Division result : " + (t/s));
	}

}
