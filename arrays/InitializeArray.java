package arrays;

public class InitializeArray {

	public static void main(String[] args) {
		int arr[] = {5,10,15,20,25,30,35,40,45,50};
		int len = arr.length;
		System.out.println("The size of the array is : " + len);
		for(int i=0;i<len;i++) {
			System.out.print(arr[i] + " ");
		}
	}

}
